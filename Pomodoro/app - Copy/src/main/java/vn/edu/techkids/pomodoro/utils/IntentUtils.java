package vn.edu.techkids.pomodoro.utils;

import android.content.Intent;

/**
 * Created by TienVV on 12/17/15.
 */
public class IntentUtils {

    public static final String INTENT_BROADCAST_TIME_ACTION = "INTENT_BROADCAST_TIME_ACTION";
    public static final String INTENT_BROADCAST_TIME_TYPE = "INTENT_BROADCAST_TIME_TYPE";
    public static final String INTENT_BOARDCAST_KEY_CURRENT_TIME = "INTENT_BOARDCAST_KEY_CURRENT_TIME";
    public static final String INTENT_BOARDCAST_KEY_MAX_TIME = "INTENT_BOARDCAST_KEY_MAX_TIME";
    public static final String INTENT_BOARDCAST_KEY_CURRENT_PRERIOD = "INTENT_BOARDCAST_KEY_CURRENT_PRERIOD";

    public static final String INTENT_BROADCAST_TYPE_INPROGRESS = "INTENT_BROADCAST_TYPE_INPROGRESS";
    public static final String INTENT_BROADCAST_TYPE_BREAKING = "INTENT_BROADCAST_TYPE_BREAKING";

    /**
     * Create intent for send information of timer
     * @return
     */
    public static Intent createIntentTimerInfo() {
        Intent result = new Intent();
        result.setAction(INTENT_BROADCAST_TIME_ACTION);
        return result;
    }
}